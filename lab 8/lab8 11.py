# With respect to a function, an argument is the value that
# are sent to a function when its called. The argument sent
# through a function call is recieved by the parameters in
# the function defenition for further calculation.
# For example :-


def add(a,b):   
    print(a+b)      
a,b=3,4
add(a,b)  # Here the variables a,b act as the argument 
          # of the function call here a,b are variable
          # with value 3 and 4.


